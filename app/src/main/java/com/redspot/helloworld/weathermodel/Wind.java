package com.redspot.helloworld.weathermodel;

public class Wind {
    private float speed;
    private int deg;

    public void setSpeed(float speed) {
        this.speed = speed;
    }

    public float getSpeed() {
        return speed;
    }

    public void setDeg(int deg) {
        this.deg = deg;
    }

    public int getDeg() {
        return deg;
    }
}
