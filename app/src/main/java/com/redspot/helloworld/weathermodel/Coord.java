package com.redspot.helloworld.weathermodel;

public class Coord {
    private float lon;
    private float lan;

    public void setLon(float lon) {
        this.lon = lon;
    }

    public float getLon() {
        return lon;
    }

    public void setLan(float lan) {
        this.lan = lan;
    }

    public float getLan() {
        return lan;
    }
}
